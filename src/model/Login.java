package model;

/**
 *
 * @author rafa_
 */
public class Login {
    private int id;
    private String login;
    private int senha;

    public Login(String login, int senha) {
        this.login = login;
        this.senha = senha;
    }

    
    
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public int getSenha() {
        return senha;
    }

    public void setSenha(int senha) {
        this.senha = senha;
    }
    
    
    
}
